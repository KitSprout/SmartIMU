/*====================================================================================================*
**函數 : Delay_1us
**功能 : 延遲 1us
**輸入 : nCnt_1us
**輸出 : None
**使用 : Delay_1us(Times);
**====================================================================================================*/
/*====================================================================================================*/
void Delay_1us( uint32_t nCnt_1us )
{
  uint32_t nCnt;
  for(; nCnt_1us != 0; nCnt_1us--)
    for(nCnt = 5; nCnt != 0; nCnt--);
}
